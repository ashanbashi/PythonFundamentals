#(1)Arithmetic Operators

x = 10
y = 5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus-returns the remainder


#(2)Comparison Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not Equal


#(3)Assignment Operators
a=56
print(a)

a+=2 #Operator Precedence - Order in which operations get executed
print(a)


#(4)Logical Operators - and, or, not
print(100 < 200 and 100 == 200) #and-states false if any of the conditions is wrong
print( 100 < 200 or 100 == 200) #Or-states true if one of the condition is true
print( not(100 < 200 or 100 == 200)) #Not-gives opposite


